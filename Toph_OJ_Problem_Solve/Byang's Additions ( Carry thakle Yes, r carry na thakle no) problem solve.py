'''
No
'''

A, B = input().split()
a = list(A)
b = list(B)
r_1 = a[::-1]
r_2 = b[::-1]
count = 0
if len(r_1) >= len(r_2):
    len = len(r_2)
else:
    len = len(r_1)
for i in range(0,len):
    sum = int(r_1[i]) + int(r_2[i])
    if sum > 9:
        count += 1
if count != 0:
    print("Yes")
else:
    print("No")
